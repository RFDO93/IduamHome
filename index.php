                if(nextIndex == 1){
                    $("#logo").attr('src','image/Menu/LogoBlanco.svg');
                    $(".nav-link").css("color","#ffffff");
                    $(".menu").removeClass('menuSinFondo');
                    playVideo();
                } else if (nextIndex == 3){
                    setTimeout(function () {
                        $("#logo").attr('src','image/Menu/LogoNegro.svg');
                        $(".nav-link").css("color","#000000");
                        $(".menu").addClass("menuSinFondo");
                        playVideo(false);
                    },500);
                }else{
                    setTimeout(function () {
                        $("#logo").attr('src','image/Menu/LogoBlanco.svg');
                        $(".nav-link").css("color","#ffffff");
                        $(".menu").addClass("menuSinFondo");
                        playVideo(false);
                    },500);
                }

                if (nextIndex == 7 || nextIndex == 8) {
                    $(".menu").css("visibility","hidden");
                }else{
                    $(".menu").css("visibility","visible");
                }


            },
        });

        //methods
        $.fn.fullpage.setAllowScrolling(true);

        let options = {
            id : 316159723,
            autoplay:true,
            loop: true
        };

        let player = new Vimeo.Player('videoBackground',options);
        let isPause = false;
        player.on('play', function () {
            isPause = true;
        });

        function playVideo(aux = true) {
            if (aux){
                player.play().then(function() {
                    // the video was played
                }).catch(function(error) {
                    switch (error.name) {
                        case 'PasswordError':
                            // the video is password-protected and the viewer needs to enter the
                            // password first
                            break;

                        case 'PrivacyError':
                            // the video is private
                            break;

                        default:
                            // some other error occurred
                            break;
                    }
                });
            } else {
                if (isPause){
                    player.pause().then(function() {
                        console.log('pause');
                        isPause = false;
                    }).catch(function(error) {
                        switch (error.name) {
                            case 'PasswordError':
                                // the video is password-protected and the viewer needs to enter the
                                // password first
                                break;

                            case 'PrivacyError':
                                // the video is private
                                break;

                            default:
                                // some other error occurred
                                break;
                        }
                    });
                }

            }

        }

    });
